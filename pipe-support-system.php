                                <p>A pipe support or pipe hanger is a designed element that transfer the load from a
                                    pipe to the supporting structures. The load includes the weight of the pipe proper,
                                    the content that the pipe carries, all the pipe fittings attached to pipe, and the
                                    pipe covering such as insulation.</p>
                                <p>The four main functions of a pipe support are to anchor, guide, absorb shock, and
                                    support a specified load. Pipe supports used in high or low temperature applications
                                    may contain insulation materials. The overall design configuration of a pipe support
                                    assembly is dependent on the loading and operating conditions.</p>

                                <h5>Primary load</h5>
                                <p>These are typically steady or sustained types of loads such as internal fluid
                                    pressure, external pressure, gravitational forces acting on the pipe such as weight
                                    of pipe and fluid, forces due to relief or blow down, pressure waves generated due
                                    to water/steam hammer effects.</p>
                                <h5>Sustained loads:</h5>
                                <ul>
                                    <li>
                                        <strong>Internal/External Pressure:</strong> A pipe used for transporting fluid
                                        would be under internal
                                        pressure load. A pipe such as a jacketed pipe core or tubes in a Shell & Tube
                                        ex-changer etc. may be under net external pressure. Internal or external
                                        pressure induces stresses in the axial as well as circumferential (Hoop Stress)
                                        directions. The pressure also induces stresses in the radial direction, but
                                        these are often neglected. The internal pressure exerts an axial force equal to
                                        pressure times the internal cross section of the pipe. F =P[Ï€d^2/4]. If outer
                                        diameter is used for calculating approximate metal cross-section as Pressure
                                        well as pipe cross-section, the axial stress can often be approximated as
                                        follows : S =Pd /(4t)
                                    </li>
                                    <li>
                                        <strong>Dead weight:</strong>It is the self weight of pipe including fluid,
                                        weight of fittings & other inline components (say valve, insulation etc.). This
                                        type of loads act throughout the life cycle of pipe. In horizontal pipes, these
                                        loads cause bending, and the bending moment is related to normal and shear
                                        stresses. Pipe bending is caused mainly due to two reasons: distributed weight
                                        load (e.g. fluid weight) and concentrated weight load (e.g. valve weight). The
                                        weight of risers (vertical sections of pipe) can be supported by riser clamps.
                                    </li>
                                </ul>
                                <h5>Occasional loads:</h5>
                                <ul>
                                    <li>
                                        <strong>Wind load:</strong> Piping which are located outdoors and thus exposed
                                        to wind will be designed to withstand the maximum wind velocity expected during
                                        the plant operating life. Wind force is modeled as a uniform load acting upon
                                        the projected length of the pipe perpendicular to the direction of the wind.
                                        Wind pressure for various elevations will be used to calculate wind force using
                                        the following formula. Fw = Pw x S x A, where Fw = The total wind force, Pw =
                                        The equivalent wind pressure, S = Wind shape factor, A = Pipe exposed area.
                                    </li>
                                    <li>
                                        <strong>Seismic load:</strong>Seismic load is one of the basic concepts of
                                        earthquake engineering which means application of an earthquake-generated
                                        agitation to a structure. It happens at contact surfaces of a structure either
                                        with the ground,[2] or with adjacent structures,[3] or with gravity waves from
                                        tsunami.
                                    </li>
                                    <li>
                                        <strong>Water hammer:</strong> Water hammer (or more generally, fluid hammer) is
                                        a pressure surge or wave caused when a fluid (usually a liquid but sometimes
                                        also a gas) in motion is forced to stop or change direction suddenly (momentum
                                        change). Water hammer commonly occurs when a valve closes suddenly at an end of
                                        a pipeline system, and a pressure wave propagates in the pipe. It's also called
                                        hydraulic shock.
                                    </li>
                                    <li>
                                        <strong>Steam hammer:</strong>Steam hammer, the pressure surge generated by
                                        transient flow of super-heated or saturated steam in a steam-line due to sudden
                                        stop valve closures is considered as an occasional load. Though the flow is
                                        transient, for the purpose of piping stress analysis, only the unbalanced force
                                        along the pipe segment tending to induce piping vibration is calculated and
                                        applied on the piping model as static equivalent force.
                                    </li>
                                    <li>
                                        <strong>Safety valve Discharge:</strong>Reaction forces from relief valve
                                        discharge is considered as an occasional load. The reaction force due to steady
                                        state flow following the opening of safety relief valve in an open discharge
                                        installation can be calculated in accordance with ASME B31.1 Appendix II and
                                        applied on the piping model as static equivalent force.
                                    </li>
                                </ul>
                                <h5>Secondary load</h5>
                                <p>Just as the primary loads have their origin in some force, secondary loads are
                                    caused by displacement of some kind. For example, the pipe connected to a
                                    storage tank may be under load if the tank nozzle to which it is connected moves
                                    down due to tank settlement. Similarly, pipe connected to a vessel is pulled
                                    upwards because the vessel nozzle moves up due to vessel expansion. Also, a pipe
                                    may vibrate due to vibrations in the rotating equipment it is attached to.</p>
                                <p>Displacement loads:</p>
                                <ul>
                                    <li>Load due to thermal expansion of pipe</li>
                                    <li>Load due to thermal movement of equipment</li>
                                </ul>
                                <p>A pipe may experience expansion or contraction once it is subjected to
                                    temperatures higher or lower respectively as compared to temperature at which it
                                    was assembled. The secondary loads are often cyclic but not always. For example,
                                    load due to tank settlement is not cyclic. The load due to vessel nozzle
                                    movement during operation is cyclic because the displacement is withdrawn during
                                    shut-down and resurfaces again after fresh start-up. A pipe subjected to a cycle
                                    of hot and cold fluid similarly undergoes cyclic loads and deformation.</p>
